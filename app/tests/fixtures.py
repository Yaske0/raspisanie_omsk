def _table_html(header, rows, ncols):
    cells = lambda vals: "".join(f"<td>{v}</td>" for v in vals)
    parts = ["<table>", "<tr>" + cells(header) + "</tr>"]
    for r in rows:
        parts.append("<tr>" + cells(r) + "</tr>")
        # строка-«гармошка» с деталями, как на реальном сайте (должна отфильтроваться)
        detail = ["О самолете  Авиакомпания: " + r[3]] + [""] * (ncols - 1)
        parts.append("<tr>" + cells(detail) + "</tr>")
    parts.append("</table>")
    # обернём в постороннюю таблицу-меню, чтобы проверить выбор нужной
    return ("<html><body><table><tr><td>меню сайта</td></tr></table>"
            + "".join(parts) + "</body></html>")


